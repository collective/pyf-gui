  <h1>About this service</h1>

  <p>This service aggregates all Plone related packages from PyPI, enhancing it with
  statistical data from GitHub. This makes it easier for people to find Plone add-ons
  and decide what to use and in which versions. The data from PyPI is updated
  once an hour and the data from GitHub every night.</p>

  <p>If you think we can improve something or found something not working, please let us know by opening an issue here:</p>

  <a href="https://github.com/collective/pyf-gui/issues">https://github.com/collective/pyf-gui/issues</a>

