<h1>About this service</h1>

We are aggreating all Plone related packages from PyPi and combining it with statistical data from GitHub.
This allows people to find Plone add-on's easier and decide what to use and in which versions.

The data from PyPi is updated once on hour and the data from GitHub every night.


